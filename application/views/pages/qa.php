<li>公開透明：資訊開放，各項資訊公開大眾檢視。</li>
<li>包容多元：尋找傳統政治下被忽略的聲音，提供發聲的管道。</li>
<li>立足點式平等：確保每個人都能夠有相等的權力影響公共支出。</li>
<li>社區導向：利己利人，從關心社區開始。 </li>
        </ul>

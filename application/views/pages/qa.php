          <li>施政準確化： 參與式預算拉近人民與政府的距離，透過提案反映老百姓的需要，使政府政策能夠符合地方需求，及時改善人民最迫切的需求，施政快又準。</li>
          <li>民主普遍化： 參與式預算提供一個發聲管道給所有人民，讓他們的需求能被聽見，實踐「城市權」與直接民主的理想。 </li>

        </ul>
